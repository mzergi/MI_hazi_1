package MI_hazi_1;

public class Point {
    private int X;
    private int Y;

    public Point(int _X, int _Y){
        X = _X;
        Y = _Y;
    }

    public void setX(int _X){
        X = _X;
    }
    public void setY(int _Y){
        Y = _Y;
    }
    public void setPoint(int _X, int _Y){
        X = _X;
        Y = _Y;
    }
    public int getX() {return X;};
    public int getY() {return Y;};
}
